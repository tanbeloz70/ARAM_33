O Artronat Rapid é um suplemento alimentar com Glucosamina, Condroitina, MSM (Metil Sulfonil Metano) e Vitamina C.
A vitamina C contribui para a normal formação de colagénio e para o funcionamento normal das cartilagens.
</DIV>
<DIV>Por Comprimido: Sulfato de Glucosamina 600 mg, Sulfato de Condroitina 200 mg, 
Cartilagem de Tubarão 125 mg, Metil Sulfonil Metano (MSM) 75 mg, Ácido L-ascórbico 50 mg; Antiaglomerante: Sais de Magnésio de Ácidos
 Gordos; Agentes de Volume: Fosfato Dicálcico, Celulose.
</DIV>
<DIV>Contém: Produtos à base de crustáceos: produtos à base de peixe

Conservar em ambiente seco e fresco, ao abrigo da luz solar.
Manter fora do alcance das crianças.
Não deve ser excedida a toma diária indicada.
Este produto não substitui um regime alimentar variado
Mulheres grávidas ou a amamentar não devem utilizar este suplemento.
Não recomendado em caso de hipersensibilidade ou alergia a qualquer um dos constituintes da formulação.
Recomendada precaução no caso de toma de Varfarina.
Não recomendado em caso de alergia a marisco ou seus derivados.
</DIV>
