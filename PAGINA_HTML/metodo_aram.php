<h1>O que é a kinesioterapia?</h1>
<p>Kinesioterapia vem do grego "kínesis", que significa movimento e " therapeia", terapia. Desta forma,
     a terapia do paciente é feita através do movimento, sendo ele passivo ou activo.
</p>
<p>Kinesioterapia é uma disciplina médica independente, com base na experiência de todas as outras disciplinas médicas 
    e inclui teoria e metodologia de educação física. O processo de tratamento passa por um trabalho conjunto entre o 
    paciente e o terapeuta que tem como base a psicologia educacional.
</p><p>
A Kinesioterapia  está  dividida em duas secções principais: ativa e passiva.

</p><ul><li>
Metodologia activa:  Participação ativa e consciente do paciente, que faz o movimento . 
</li><li>
Metodologia passiva: abrange as modalidades e os meios em que o paciente participa
 passivamente e faz movimentos involuntários. O movimento realizado por outra pessoa, ou 
 com máquinas e/ou dispositivos especiais, imitam o movimento fisiológico normal (fisiologia do exercício passivo).
  Realizam-se movimentos de tecidos individuais ou partes do corpo com a ajuda de manobras especialmente organizadas e 
  metódicas (massagem, manipulação de mão, procedimentos mecanoterapia , eletroestimulação).
</li>
</ul>

<p>O uso adequado de exercícios recreativos realizados em ginásios de reabilitação específicos aliviam a tensão das 
    articulações e em particular as da coluna vertebral, melhorando:
</p>
 
<ul><li>
O fluxo sanguíneo.</li><li>

Restauro das propriedades básicas do tecido muscular melhorando a elasticidade. 
</li><li>
Elimina espasmos e dor, (incluindo o período mais agudo) 
</li><li>
Acelera o processo de auto-regeneração para normalização da cartilagem articular e do disco intervertebral, sem cirurgia. 
</li>
</ul>
<p>
O plano de recuperação é SEMPRE feito individualmente, com base nas características físicas únicas e condição do paciente.
</p>
<p> 
Os exercícios de reabilitação foram especialmente concebidos para:
</p>
 
<ul><li>
Melhorar a mobilidade articular;</li>
<li>Aumentar a elasticidade dos músculos, ligamentos e tendões;</li>
<li>Fortalecer os músculos profundos adjacentes à coluna e grandes articulações.
</li></ul>

<h1>Quais são as patologias que podem ser tratadas?</h1>
<p>
Todas as disfunções de origem músculo esqueléticas; hérnias discais, tendinites, artroses, 
artrites, entorses, recuperação de pós-operatório.</p>

<h1>Que mais a kinesioterapia trata? </h1>
<p>
Além de todas as disfunções anteriores, a kinesioterapia restabelece o equilíbrio postural, bem como dos diversos sistemas
 do corpo humano, através de exercícios especializados para cada tipo de patologia.
</p>

