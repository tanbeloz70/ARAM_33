<h1>Curso de Kinesioterapia</h1>

No ARAM queremos que cada vez mais as pessoas tenham acesso aos benefícios da kinesioterapia. 
Durante os vários anos de prática clínica tivémos a manifestação de interesse pela nossa prática reabilitativa 
tanto por parte de pacientes como de outras pessoas ligadas ao campo da saúde, por isso desenvolvemos 
um curso de formação em kinesioterapia. 
</div>
<div>
 <h3>   
Para quem é direcionada esta formação?</h3>

Este curso é direcionado para quaisquer terapeutas de saúde que visam a recuperação terapêutica dos
 seus pacientes/clientes; técnicos auxiliares de fisioterapia; fisioterapeutas; massagistas; enfermeiros;
  personal trainers; ginásios que queiram melhorar a qualidade dos seus serviços; osteopatas e outros 
  terapeutas da saúde reabilitativa.
</div>
<div>
<h3>   Quais são as patologias que podem ser tratadas?</h3>
Todas as disfunções de origem músculo-esqueléticas; hérnias discais, tendinites, artroses, artrites, entorses,
 recuperação de pós-operatório ou simplesmente reeducação postural.
</div>
 
<div>
Além de todas as disfunções anteriores, a kinesioterapia restabelece o equilíbrio postural, bem como dos diversos sistemas do corpo humano, atravez de exercícios especializados.
</div>
<div>
<h3>
A estrutura do curso:</h3>

O curso de kinesioterpia é dividido por Módulos, cada um com o seu objectivo. Cada módulo é dividido por aulas em que 
cada uma tem a duração de 6 horas.
A aula é dividida numa parte prática e outra teórica, o restante tempo é dedicado ao esclarecimento de dúvidas relativas a aula dada.
</div>

